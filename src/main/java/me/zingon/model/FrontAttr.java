package me.zingon.model;

/**
 * 前端属性
 * @author Administrator
 *
 */
public class FrontAttr {
	
	/**
	 * 前端类型 input , textarea , date , selectbox ,
	 */
	private String type; 

	/**
	 * 是否必填 0非必填 1 必填
	 */
	private int isNull;
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getIsNull() {
		return isNull;
	}

	public void setIsNull(int isNull) {
		this.isNull = isNull;
	}
	
	
	
	
}
